package org.semantictools.context.renderer;

public class ImageInfo {
  
  private int height;
  private int width;
  
  public ImageInfo(int height, int width) {
    this.height = height;
    this.width = width;
  }
  public int getHeight() {
    return height;
  }
  public int getWidth() {
    return width;
  }
  
  

}
